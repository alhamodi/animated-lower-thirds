window.i18nData = i18nData;

function setLanguage(lang) {
  if (!i18nData[lang]) lang = 'ar';
  
  // Set global language and direction
  document.documentElement.lang = lang;
  document.documentElement.dir = lang === 'ar' ? 'rtl' : 'ltr';
  localStorage.setItem('appLang', lang);

  const t = i18nData[lang];

  // 1. Text Content
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    if (t[key]) {
      // Keep icon if exists inside button
      if (el.tagName === 'BUTTON' && el.querySelector('span.icon')) {
          const iconHTML = el.querySelector('span.icon').outerHTML;
          el.innerHTML = iconHTML + ' ' + t[key];
      } else {
          el.textContent = t[key];
      }
    }
  });

  // 2. Placeholders
  document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
    const key = el.getAttribute('data-i18n-placeholder');
    if (t[key]) {
      el.placeholder = t[key];
    }
  });

  // 3. Tooltips (title attribute)
  document.querySelectorAll('[data-i18n-title]').forEach(el => {
    const key = el.getAttribute('data-i18n-title');
    if (t[key]) {
      el.title = t[key];
    }
  });

  // Re-render Styles grid if controller is loaded
  if (window.renderStylesGrid) {
    window.renderStylesGrid();
  }
}

// Init language on load
document.addEventListener('DOMContentLoaded', () => {
  const savedLang = localStorage.getItem('appLang') || 'ar';
  setLanguage(savedLang);
  
  const langSwitcher = document.getElementById('langSwitcher');
  if (langSwitcher) {
    langSwitcher.value = savedLang;
    langSwitcher.addEventListener('change', (e) => {
      setLanguage(e.target.value);
    });
  }
});
